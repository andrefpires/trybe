let a = 6;
let b = 8;

// Adição
let soma = a + b;
console.log(soma);

// Subtração
let subtracao = a - b;
console.log(subtracao);

// Multiplicação
let multiplicacao = a * b;
console.log(multiplicacao);

// Divisão
let divisao = a / b;
console.log(divisao);

// Módulo (diferença)
let modulo = a % b;
console.log(modulo);
